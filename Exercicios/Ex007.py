'''Crie um programa que leia duas notas de um aluno e mostre na tela sua media'''

nota1 = float(input('Digite a primeira nota: '))
nota2 = float(input('Digite a segunda nota: '))

print(f'A nota do primeiro aluno foi {nota1} e do segundo foi {nota2} a media entre os dois resulta em {(nota1 + nota2)/2}')